package com.ucentral.modelo;

/**
 *
 * @author luisfernandotorressiles
 */
public class Saloneros {
    
    public Integer idSalonero;
    public String nombre;
    public String apellidos;

    public Integer getIdSalonero() {
        return idSalonero;
    }

    public String getNombre() {
        return nombre;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setIdSalonero(Integer idSalonero) {
        this.idSalonero = idSalonero;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }
    
    
    
}
